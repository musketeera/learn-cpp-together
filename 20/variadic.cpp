#include <iostream>
using namespace std;

//定义一个接收可变参数的宏函数
#define LOG(o,...) fprintf(o,"[%s:%d]",__FILE__,__LINE__);\
                   fprintf(o,__VA_ARGS__)

int main(void){
    //使用定义的宏打印一条日志
    LOG(stdout,"%s\n","测试信息,日志001");
}